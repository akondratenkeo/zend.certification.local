/**
 * Для сравнения чисел с плавающей точкой используется верхняя граница относительной ошибки при округлении.
 * Эта величина называется машинной эпсилон или единицей округления (unit roundoff) и представляет собой самую
 * маленькую допустимую разницу при расчетах.
 */
$a = 1.23456789;
$b = 1.23456780;
$epsilon = 0.00001;

if (abs($a - $b) < $epsilon) {
    echo "true";
}

